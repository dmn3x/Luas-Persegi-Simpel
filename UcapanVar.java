package tipedata;

public class UcapanVar {

    public static void main(String[] args) {
        String kalPertama = "Halo ULM",
               kalKedua = "ULM Berdaya Saing dan Unggul di Bidang Lahan Basah",
               kalKetiga = "Prodi Ilkom ULM Unggul dengan Teknologi Kekinian";
        System.out.println(kalPertama);
        System.out.println(kalKedua);
        System.out.println(kalKetiga);
        System.out.println("--------");
        System.out.println(kalPertama + "\n" + kalKedua + "\n" + kalKetiga);
        System.out.println("--------");
        System.out.println(kalPertama + "\n \t" + kalKedua);
    }
}
